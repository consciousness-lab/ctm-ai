def run_instance(test_file, dataset, dataset_name, output_file,
                 ctm_prompts, pipeline_prompts, model):
    try:
        config = get_dataset_config(dataset_name)
        sample = dataset[test_file]
        target_sentence = config.get_text_field(sample)
        label = config.get_label_field(sample)
        query = config.get_task_query()

        audio_path = get_audio_path(test_file, dataset_name)
        video_path = get_muted_video_path(test_file, dataset_name)
        if not os.path.exists(audio_path):
            audio_path = None
        if not os.path.exists(video_path):
            video_path = None

        synth_prompt, critic_prompt, judge_prompt = pipeline_prompts
        start_time = time.time()
        reports, usage_stats = asyncio.run(run_metagpt_sample_async(
            query=query, target_sentence=target_sentence,
            audio_path=audio_path, video_path=video_path,
            text_system=ctm_prompts['language_processor']['system_prompt'],
            audio_system=ctm_prompts['audio_processor']['system_prompt'],
            video_system=ctm_prompts['video_processor']['system_prompt'],
            synthesizer_system=synth_prompt,
            critic_system=critic_prompt,
            judge_system=judge_prompt,
            model=model,
        ))
        elapsed = time.time() - start_time

        final_answer = reports.get('judgment', '') or ''
        parsed = _parse_yes_no(final_answer)
        print(
            f'[{test_file}] {elapsed:.1f}s | api={usage_stats["total_api_calls"]} '
            f'tok={usage_stats["total_tokens"]} | {final_answer[:60]}'
        )

        result = {
            test_file: {
                'answer': [final_answer],
                'parsed_answer': [parsed],
                'label': label,
                'method': 'metagpt_framework',
                'latency': elapsed,
                'api_calls': usage_stats['total_api_calls'],
                'prompt_tokens': usage_stats['total_prompt_tokens'],
                'completion_tokens': usage_stats['total_completion_tokens'],
                'total_tokens': usage_stats['total_tokens'],
                'stage_latencies': usage_stats['stage_latencies'],
                'stage_outputs': {
                    'text_report': reports.get('text', ''),
                    'audio_report': reports.get('audio', ''),
                    'video_report': reports.get('video', ''),
                    'synthesis': reports.get('synthesis', ''),
                    'critique': reports.get('critique', ''),
                },
            }
        }

        with file_lock:
            with open(output_file, 'a', encoding='utf-8') as f:
                f.write(json.dumps(result, ensure_ascii=False) + '\n')
        return f'OK {test_file}'

    except Exception as e:
        print(f'[{test_file}] ERROR: {e}')
        import traceback
        traceback.print_exc()
        return f'FAIL {test_file}: {e}'


