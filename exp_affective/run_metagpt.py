# =============================================================================
# Shared LLM call (bypasses metagpt's _aask so the LLM surface matches other
# baselines). Supports text-only and multimodal message lists.
# =============================================================================

def _call_llm(messages, model, max_retries=3):
    """Call litellm.completion with retry. Returns (text, usage)."""
    start = time.time()
    api_calls = 0
    for attempt in range(1, max_retries + 1):
        try:
            api_calls += 1
            resp = litellm.completion(
                model=model, messages=messages, temperature=0.0,
            )
            text = resp.choices[0].message.content
            usage = {
                'prompt_tokens': getattr(resp.usage, 'prompt_tokens', 0) or 0,
                'completion_tokens': getattr(resp.usage, 'completion_tokens', 0) or 0,
                'total_tokens': getattr(resp.usage, 'total_tokens', 0) or 0,
                'api_calls': api_calls,
                'latency': time.time() - start,
            }
            if text:
                return text.strip(), usage
        except Exception as e:
            print(f'  LLM attempt {attempt}/{max_retries} failed: {e}')
            if attempt < max_retries:
                time.sleep(2 ** attempt)
    return None, {
        'prompt_tokens': 0, 'completion_tokens': 0, 'total_tokens': 0,
        'api_calls': api_calls, 'latency': time.time() - start,
    }


def _encode_file(path):
    with open(path, 'rb') as f:
        return base64.b64encode(f.read()).decode('utf-8')


def _audio_mime(path):
    ext = path.split('.')[-1].lower()
    return {
        'mp3': 'audio/mp3', 'wav': 'audio/wav',
        'mp4': 'audio/mp4', 'aac': 'audio/aac',
    }.get(ext, 'audio/mp4')


