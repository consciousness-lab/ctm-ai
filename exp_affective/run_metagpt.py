"""
MetaGPT multi-agent baseline for affective detection (MUStARD / URFunny).

This baseline is built on the *real* MetaGPT framework: it imports
`metagpt.actions.Action`, `metagpt.actions.UserRequirement`,
`metagpt.roles.Role`, `metagpt.team.Team`, and `metagpt.schema.Message`,
and uses them exactly the way MetaGPT's own example
`MetaGPT/examples/build_customized_multi_agents.py` does.

6 roles, wired through MetaGPT's `_watch` message-routing so the SOP
progresses automatically as each role publishes its output:

    UserRequirement
         │
         ├─ TextAnalyst    ── TextAnalysisAction    ┐
         ├─ AudioAnalyst   ── AudioAnalysisAction   ├─► Synthesizer
         └─ VideoAnalyst   ── VideoAnalysisAction   ┘    │
                                                         ▼
                                                      Critic
                                                         │
                                                         ▼
                                                       Judge

Rounds: 4 (modalities → synthesis → critic → judge). Each round runs all
non-idle roles concurrently via MetaGPT's Environment.run().

Because MetaGPT's `_aask` uses its own LLM layer, we bypass it inside our
custom Actions and call `litellm.completion` directly — this keeps the LLM
call surface identical to run_moa.py / run_autogen.py for fair comparison,
so the only thing that differs between baselines is the *orchestration*.

REQUIRES: `pip install metagpt` in a dedicated env (numpy pinned to 1.26).

Examples:
    python run_metagpt.py --dataset_name mustard --max_workers 4
    python run_metagpt.py --dataset_name urfunny --resume
"""

