# =============================================================================
# MetaGPT Roles
# =============================================================================
#
# Each Role subclasses metagpt.roles.Role. It:
#   1. Uses `_watch(...)` to subscribe to upstream Actions
#   2. Uses `set_actions(...)` to install its own Action(s)
#   3. Overrides `_act()` to thread sample-specific data into its Action
#
# Sample data (target_sentence, audio_path, video_path, query) is stored as
# Pydantic fields on the Role instance. We create a fresh set of roles per
# sample (roles are stateful in MetaGPT).
# =============================================================================

class _BaseAnalyst(Role):
    """Base for modality analyst roles watching UserRequirement."""
    query: str = ''
    target_sentence: str = ''
    audio_path: Optional[str] = None
    video_path: Optional[str] = None
    model: str = DEFAULT_MODEL
    # Captures the Action's output so the driver can read it directly without
    # replaying MetaGPT's broadcast-and-observe cycle through another round.
    last_output: str = ''


class TextAnalyst(_BaseAnalyst):
    name: str = 'TextAnalyst'
    profile: str = 'Text Modality Analyst'

    def __init__(self, **kwargs):
        super().__init__(**kwargs)
        system_prompt = kwargs.get('system_prompt', '')
        action = TextAnalysisAction(
            system_prompt=system_prompt, model=self.model,
        )
        self.set_actions([action])
        self._watch([UserRequirement])

    async def _act(self) -> Message:
        todo: TextAnalysisAction = self.rc.todo  # type: ignore[assignment]
        result = await todo.run(
            query=self.query, target_sentence=self.target_sentence,
        )
        self.last_output = result
        msg = Message(
            content=result, role=self.profile, cause_by=TextAnalysisAction,
            sent_from=self.name,
        )
        self.rc.memory.add(msg)
        return msg


