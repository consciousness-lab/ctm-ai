class Synthesizer(Role):
    name: str = 'Synthesizer'
    profile: str = 'Cross-Modal Synthesizer'
    query: str = ''
    model: str = DEFAULT_MODEL
    last_output: str = ''

    def __init__(self, **kwargs):
        super().__init__(**kwargs)
        system_prompt = kwargs.get('system_prompt', '')
        action = SynthesisAction(
            system_prompt=system_prompt, model=self.model,
        )
        self.set_actions([action])
        # Synthesizer becomes non-idle once any modality analysis arrives,
        # but we gate actual execution on having *all three* via _think().
        self._watch([TextAnalysisAction, AudioAnalysisAction, VideoAnalysisAction])

    async def _think(self) -> bool:
        # Wait until memory contains at least one message from each of the
        # three modality experts. MetaGPT's env.run() runs all roles in a
        # single asyncio.gather per round, which means downstream roles can
        # observe partial upstream output during the same round and react
        # early — producing duplicate downstream actions. Gating on memory
        # contents here ensures Synthesizer fires exactly once when the
        # full set of modality reports is available.
        cbs = [str(m.cause_by) for m in self.rc.memory.get()]
        has_text = any('TextAnalysisAction' in c for c in cbs)
        has_audio = any('AudioAnalysisAction' in c for c in cbs)
        has_video = any('VideoAnalysisAction' in c for c in cbs)
        if not (has_text and has_audio and has_video):
            self._set_state(-1)
            return False
        self._set_state(0)
        return True

    def _collect_reports(self) -> Dict[str, str]:
        """Pull the latest text/audio/video reports from our own memory.
        Message.cause_by is normalised to a class-path string by MetaGPT's
        `any_to_str` validator, so we match by substring."""
        reports = {'text': '[text missing]', 'audio': '[audio missing]',
                   'video': '[video missing]'}
        for msg in self.rc.memory.get():
            cb_str = str(msg.cause_by)
            if 'TextAnalysisAction' in cb_str:
                reports['text'] = msg.content
            elif 'AudioAnalysisAction' in cb_str:
                reports['audio'] = msg.content
            elif 'VideoAnalysisAction' in cb_str:
                reports['video'] = msg.content
        return reports

    async def _act(self) -> Message:
        todo: SynthesisAction = self.rc.todo  # type: ignore[assignment]
        reports = self._collect_reports()
        result = await todo.run(
            query=self.query,
            text_report=reports['text'],
            audio_report=reports['audio'],
            video_report=reports['video'],
        )
        self.last_output = result
        msg = Message(
            content=result, role=self.profile, cause_by=SynthesisAction,
            sent_from=self.name,
        )
        self.rc.memory.add(msg)
        return msg


class Critic(Role):
    name: str = 'Critic'
    profile: str = 'Critical Reviewer'
    query: str = ''
    model: str = DEFAULT_MODEL
    last_output: str = ''
    # In metagpt 0.8.2 Role._observe() already adds every popped message to
    # memory (only rc.news is filtered), so Critic's memory naturally retains
    # the upstream modality reports published in round 1 even though it
    # only *reacts* to SynthesisAction in round 3.

    def __init__(self, **kwargs):
        super().__init__(**kwargs)
        system_prompt = kwargs.get('system_prompt', '')
        action = CriticAction(system_prompt=system_prompt, model=self.model)
        self.set_actions([action])
        self._watch([SynthesisAction])

    async def _think(self) -> bool:
        cbs = [str(m.cause_by) for m in self.rc.memory.get()]
        needed = ['TextAnalysisAction', 'AudioAnalysisAction',
                  'VideoAnalysisAction', 'SynthesisAction']
        if not all(any(n in c for c in cbs) for n in needed):
            self._set_state(-1)
            return False
        self._set_state(0)
        return True

    def _collect_reports(self) -> Dict[str, str]:
        reports = {'text': '[text missing]', 'audio': '[audio missing]',
                   'video': '[video missing]', 'synthesis': '[synthesis missing]'}
        for msg in self.rc.memory.get():
            cb_str = str(msg.cause_by)
            if 'TextAnalysisAction' in cb_str:
                reports['text'] = msg.content
            elif 'AudioAnalysisAction' in cb_str:
                reports['audio'] = msg.content
            elif 'VideoAnalysisAction' in cb_str:
                reports['video'] = msg.content
            elif 'SynthesisAction' in cb_str:
                reports['synthesis'] = msg.content
        return reports

    async def _act(self) -> Message:
        todo: CriticAction = self.rc.todo  # type: ignore[assignment]
        reports = self._collect_reports()
        result = await todo.run(
            query=self.query,
            text_report=reports['text'],
            audio_report=reports['audio'],
            video_report=reports['video'],
            synthesis=reports['synthesis'],
        )
        self.last_output = result
        msg = Message(
            content=result, role=self.profile, cause_by=CriticAction,
            sent_from=self.name,
        )
        self.rc.memory.add(msg)
        return msg


